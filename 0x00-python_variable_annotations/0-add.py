#!/usr/bin/env python3
"""This module takes 2 numbers and adds them together."""


def add(a: float, b: float) -> float:
    """
    This function takes two floats and sums them up together

    Args:
        a (int): The first argument
        b (float): the second argument

    Returns:
        (float) the result of adding the 2 numbers together
    """
    return a + b
