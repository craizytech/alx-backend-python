#!/bin/bash/env python3
"""This modules takes a float and returns the floor of the float"""


import math


def floor(n: float) -> int:
    """
    This method takes a float number and floors it to become a whole
    number

    Args:
        n (float): this is the 1st argument

    Returns:
        (int): this function returns an int
    """

    return math.floor(n)
