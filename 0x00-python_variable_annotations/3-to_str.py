#!/usr/bin/env python3
"""
This module takes a float n as an argument and
returns the string rep of the float
"""


def to_str(n: float) -> str:
    """
    This function takes a float and returns it's string rep

    Args:
        n (float): this is the only float argument

    Returns:
        (str): the result of converting the float to a string.
    """
    return str(n)
